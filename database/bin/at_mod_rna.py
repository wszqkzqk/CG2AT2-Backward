exclude_atoms = ["H5'1", "H5T", "H5'2", "H2'1", "H2'2", "H3T"]
def build_multi_residue_atomistic_system(cg_residues, sys_type, mapping):
    chain_count=1
    gen.mkdir_directory(g_var.working_dir+sys_type)  ### make and change to protein 
    chain_now = None
    g_var.seq_cg={sys_type:{}}
    g_var.seq_cg[sys_type][chain_count]=[]
    g_var.ter_res={sys_type:{}}
    out = []
    cg_residue_last = None
    for cg_residue_id, cg_residue in cg_residues[sys_type].items():
        resname = cg_residue[next(iter(cg_residue))]['residue_name']
        chain_id = cg_residue[next(iter(cg_residue))]['chain_id']
        if cg_residue_last is not None:
            if breaks(cg_residue_last, cg_residue):
                out_new = []
                for o in out:
                    out_new.extend(o)
                out = out_new
                g_var.ter_res[sys_type][chain_count-1][1]=ter
                # chain_break = breaks(out)
                write_pdb(out, chain_count-1, sys_type)
                out = []
                chain_count += 1
                chain_now = chain_id
                g_var.seq_cg[sys_type][chain_count]=[]
                g_var.ter_res[sys_type][chain_count-1]=[resname, False]
        if chain_now == None:
            chain_now = chain_id
            g_var.ter_res[sys_type][chain_count-1]=[resname, False]
        elif chain_now != chain_id:
            out_new = []
            for o in out:
                out_new.extend(o)
            out = out_new
            g_var.ter_res[sys_type][chain_count-1][1]=ter
            write_pdb(out, chain_count-1, sys_type)
            out = []
            chain_count += 1
            chain_now = chain_id
            g_var.seq_cg[sys_type][chain_count]=[]
            g_var.ter_res[sys_type][chain_count-1]=[resname, False]
        g_var.seq_cg[sys_type] = add_to_sequence(g_var.seq_cg[sys_type], resname, chain_count)
        residue = [(bead, resi['residue_name'], cg_residue_id, chain_id, float(resi['coord'][0]), float(resi['coord'][1]), float(resi['coord'][2])) for bead, resi in cg_residue.items()]
        o, r = mapping[resname].do(residue)
        o_new = []
        for atom in o:
            if atom[0] not in exclude_atoms:
                o_new.append(atom)
        o = o_new
        out.append(o)
        ter = resname
        cg_residue_last = cg_residue

    g_var.ter_res[sys_type][chain_count-1][1]=ter
    g_var.system[sys_type]=chain_count
    out_new = []
    for o in out:
        out_new.extend(o)
    out = out_new
    write_pdb(out, chain_count-1, sys_type)

def breaks(residue1, residue2):
    bb1 = residue1['BB3']['coord']
    bb2 = residue2['BB1']['coord']

    return gen.calculate_distance(bb1, bb2) > 5


def add_to_sequence(sequence, residue, chain_count):
    sequence[chain_count]+=residue 
    return sequence

def write_pdb(out, chain_id, sys_type):
    output = f'{g_var.working_dir}{sys_type}/{sys_type}_de_novo_{chain_id}.pdb'
    dev = open(output, 'w')
    dev.write("TITLE     GENERATED BY CG2AT\nREMARK    Please don\'t explode\nREMARK    Good luck\n"+g_var.box_vec+"MODEL        1\n")
    idx = 1
    for atom in out:
        nam,res,id,chn,x,y,z = atom
        dev.write(g_var.pdbline%((idx,nam,res,' ',\
                                id,x,y,z,1,0))+'\n')
        idx += 1
    dev.close()

def write_gro(out, chain_id, sys_type):
    output = f'{g_var.working_dir}{sys_type}/{sys_type}_de_novo_{chain_id}.gro'
    dev = open(output, 'w')
    dev.write("TITLE     GENERATED BY Backward\n")
    dev.write("%5d\n"%len(out))
    idx = 1
    for atom in out:
        nam,res,id,chn,x,y,z = atom
        dev.write("%5d%-5s%5s%5d%8.3f%8.3f%8.3f\n"%(id%1e5,res,nam,idx%1e5,x,y,z))
        idx += 1
    dev.write(groBoxString(pdbBoxRead(g_var.box_vec))+"\n")
    dev.close()

d2r = 3.14159265358979323846264338327950288/180
def pdbBoxRead(a):
    fa, fb, fc, aa, ab, ac = [float(i) for i in a.split()[1:7]]
    ca, cb, cg, sg         = math.cos(d2r*aa), math.cos(d2r*ab), math.cos(d2r*ac) , math.sin(d2r*ac)
    wx, wy                 = 0.1*fc*cb, 0.1*fc*(ca-cb*cg)/sg
    wz                     = math.sqrt(0.01*fc*fc - wx*wx - wy*wy)
    return [[0.1*fa, 0, 0], [0.1*fb*cg, 0.1*fb*sg, 0], [wx, wy, wz]]

def groBoxString(box):
    groBoxLine = "%10.5f%10.5f%10.5f%10.5f%10.5f%10.5f%10.5f%10.5f%10.5f"
    if box:
        return groBoxLine % (box[0][0],box[1][1],box[2][2],
                                box[0][1],box[0][2],box[1][0],
                                box[1][2],box[2][0],box[2][1])
    else:
        return groBoxLine % (0,0,0,0,0,0,0,0,0)